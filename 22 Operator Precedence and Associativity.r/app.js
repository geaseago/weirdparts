//  BIG WORD
//    OPERATOR PRECEDENCE    - The order in which operator functions get called.
//                             Higher precedence goes first.
//    OPERATOR ASSOCIATIVITY - Which direction operator functions get called in. Left or Right.
//                             Operator functions with same operator precedence value will 
//                               get called in order of their Associativity. 
